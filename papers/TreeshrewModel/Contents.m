% ISETBioPaperAndGrantCode/papers/TreeshrewModel
%
% Routines that plot various aspects of the ISETBio tree shrew model data 
%
% - plotPreretinalPigmentTransmittances  
%       Plots the transmittance of the treeshrew lens and macular pigment (absent). 
%       Also generates plots of the equivalent data (lens, macular pigment)for human retina
%
% - plotConeQuantalEfficienties
%       Plots the quantal efficiencires of the threeshew cones (L and S),
%       referred either at the cornea or at the retina.
%
% - plotConeMosaics
%       Plots cone mosaics of the treeshrew and of the human retina of the same
%       size in microns
%
% - plotTrewShrewOptics
%       Plots typical treeshrew and human optics (Polans data set)
%
% - plotPopulationTrewShrewOptics
%       Plots the population MTFs of all treeshrew subjects
%
% - plotConeMosaicsWithPSFs
%       Combo plotting of cone mosaics & PSF of the treeshrew and of the human retina 
%
% - plotLongitudinalChromaticAberrationDefocus
%       Plots the LCA the treeshrew and of the human retina
%
% - exampleComputation
%       Compute the response of a tree shrew cone mosaic to a grating