function plotRieke2017PhotocurrentResponseKineticsFromFig7D()

%These photocurrent response data are digitized from Fig 7D of 
%  "Cellular and Circuit Mechanisms Shaping the Perceptual Properties of the Primate Fovea" by 
%  Sinha, Hoon, Baudin, Okawa, Wong, & Rieke, Cell (2017), doi:10.1016/j.cell.2017.01.005
% The photocurrent response is derived by probing cones with Gaussian White
% noise. These data are from a foveal cone and from a peripheral cone. 

    theResponseData = getFovealPhotocurrentResponseData();

    plotTheResponse(theResponseData, 'fovealCone.pdf')
end


function plotTheResponse(theResponseData, pdfFileName)

    

    ff = PublicationReadyPlotLib.figureComponents('1x1 standard figure', ...
        'darkScheme', true);

    % Dark scheme
    theBackgroundColor = [33 40 42]/255;
    theLighterBackgroundColor = [43 50 52]/255;
    theLabelColor = [0.9 0.9 0.9];

    ff.backgroundColor = theLighterBackgroundColor;
    ff.titleColor = theLabelColor;
    ff.legendBackgroundColor = theBackgroundColor;
    ff.legendTextColor = theLabelColor;
    ff.axisColor = theLabelColor;

	hFig = figure(1); clf;
    theAxes = PublicationReadyPlotLib.generatePanelAxes(hFig,ff);
    ax = theAxes{1,1};

    scatter(ax, theResponseData.temporalSupportSeconds*1e3, theResponseData.pCurrentAmplitudeNormalized, ...
        20*20, 'MarkerFaceColor', 0.5*([1 0.5 0]+[1 1 1]), 'MarkerEdgeColor',  [1 0.5 0], 'MarkerFaceAlpha', 0.5, 'LineWidth', 2)
    hold(ax, 'on');

    plot(ax, theResponseData.temporalSupportSeconds*1e3, theResponseData.pCurrentAmplitudeNormalized, ...
        'r-', 'LineWidth', 4.0, 'Color', [1 0.5 0]);
    
    xlabel(ax, 'time (msec)');
    ylabel(ax, 'pCurrent (norm.)');

    xLims = [0 150];
    yLims = [-0.2 1.02];
    xTicks = 0:25:150;
    yTicks = -0.2:0.2:1;

    set(ax, 'XLim', xLims, 'YLim', yLims, 'XTick', xTicks, 'YTick', yTicks);
    PublicationReadyPlotLib.applyFormat(ax,ff);
    PublicationReadyPlotLib.offsetAxes(ax, ff, xLims, yLims); 

    % Generate figure dir if it does not exist
    theFiguresDir = ISETBioPaperAndGrantCodeFigureDirForScript(mfilename);

    thePDFfileName = fullfile(theFiguresDir, pdfFileName);
    NicePlot.exportFigToPDF(thePDFfileName,hFig,  300);
    NicePlot.exportFigToPNG(strrep(thePDFfileName, 'pdf', 'png'),hFig,  300);
    
end

